// ─── generateSchemaCode — column type mapping ─────────────────────────────────

describe('generateSchemaCode — column mapping', () => {
  const AllTypesSchema = z.object({
    id: z.string(),
    count: z.number().int(),
    score: z.number(),
    active: z.boolean(),
    createdAt: z.date(),
    status: z.enum(['a', 'b']),
    nickname: z.string().optional(),
    bio: z.string().nullable(),
    tag: z.string().default('none'),
  });

  const defs = defineStore({
    items: defineTable({
      tableName: 'items',
      schema: AllTypesSchema,
      primaryKey: 'id',
    }),
  });

  const code = generateSchemaCode(defs);

  it('includes the auto-generated header', () => {
    expect(code).toContain('// Auto-generated by @inntend/store');
  });

  it('imports only the column functions that are used', () => {
    expect(code).toContain('import {');
    expect(code).toContain('integer');
    expect(code).toContain('primaryKey');
    expect(code).toContain('real');
    expect(code).toContain('sqliteTable');
    expect(code).toContain('text');
    // index/unique not used here
    expect(code).not.toContain("'index'");
  });

  it('maps z.string() → text().notNull()', () => {
    expect(code).toContain("id: text('id').notNull()");
  });

  it('maps z.number().int() → integer().notNull()', () => {
    expect(code).toContain("count: integer('count').notNull()");
  });

  it('maps z.number() → real().notNull()', () => {
    expect(code).toContain("score: real('score').notNull()");
  });

  it('maps z.boolean() → integer({ mode: boolean }).notNull()', () => {
    expect(code).toContain(
      "active: integer('active', { mode: 'boolean' }).notNull()",
    );
  });

  it('maps z.date() → integer({ mode: timestamp_ms }).notNull()', () => {
    expect(code).toContain(
      "createdAt: integer('createdAt', { mode: 'timestamp_ms' }).notNull()",
    );
  });

  it('maps z.enum() → text().notNull()', () => {
    expect(code).toContain("status: text('status').notNull()");
  });

  it('optional field → no .notNull()', () => {
    expect(code).toContain("nickname: text('nickname')");
    expect(code).not.toContain("nickname: text('nickname').notNull()");
  });

  it('nullable field → no .notNull()', () => {
    expect(code).toContain("bio: text('bio')");
    expect(code).not.toContain("bio: text('bio').notNull()");
  });

  it('field with .default() → .notNull().default(value)', () => {
    expect(code).toContain(`tag: text('tag').notNull().default("none")`);
  });
});

// ─── generateSchemaCode — z.object() JSON field ──────────────────────────────

describe('generateSchemaCode — z.object() JSON field', () => {
  const defs = defineStore({
    items: defineTable({
      tableName: 'items',
      schema: z.object({
        id: z.string(),
        meta: z.object({ label: z.string(), count: z.number() }),
        tags: z.object({ list: z.array(z.string()) }).optional(),
      }),
      primaryKey: 'id',
    }),
  });
  const code = generateSchemaCode(defs, { settings: false });

  it("maps z.object() → text('field', { mode: 'json' }).notNull()", () => {
    expect(code).toContain("meta: text('meta', { mode: 'json' }).notNull()");
  });

  it("maps z.object().optional() → text('field', { mode: 'json' }) (no notNull)", () => {
    expect(code).toContain("tags: text('tags', { mode: 'json' })");
    expect(code).not.toContain(
      "tags: text('tags', { mode: 'json' }).notNull()",
    );
  });

  it('includes text in imports', () => {
    expect(code).toContain('text');
  });

  it('throws when indexing a JSON field', () => {
    const badDefs = defineStore({
      items: defineTable({
        tableName: 'items',
        schema: z.object({ id: z.string(), meta: z.object({ x: z.number() }) }),
        primaryKey: 'id',
        indexes: [{ columns: ['meta'] }],
      }),
    });
    expect(() => generateSchemaCode(badDefs, { settings: false })).toThrow(
      /cannot index JSON field/,
    );
  });
});

