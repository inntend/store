describe('unwrapZodType — branch coverage', () => {
  it('handles a type whose innerType is undefined (line-33 break)', () => {
    const fakeOptional = { _def: { innerType: undefined } };
    Object.defineProperty(fakeOptional, 'constructor', {
      value: { name: 'ZodOptional' },
    });
    const result = unwrapZodType(fakeOptional);
    expect(result.required).toBe(false);
    expect(result.type).toBeUndefined();
  });

  it('handles a type with no constructor name (line-22 ?? "" branch)', () => {
    // Object.create(null) has no prototype so constructor?.name is undefined.
    const noProto = Object.create(null);
    const result = unwrapZodType(noProto);
    // ctor === '' → falls to break immediately; type is returned as-is.
    expect(result.type).toBe(noProto);
    expect(result.required).toBe(true);
  });
});

