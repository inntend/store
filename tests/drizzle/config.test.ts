// ─── generateSchemaCode — primary key + indexes ───────────────────────────────

describe('generateSchemaCode — primary key and indexes', () => {
  it('single PK in extras block', () => {
    const defs = defineStore({
      users: defineTable({
        tableName: 'users',
        schema: z.object({ id: z.string(), name: z.string() }),
        primaryKey: 'id',
      }),
    });
    const code = generateSchemaCode(defs);
    expect(code).toContain('primaryKey({ columns: [t.id] })');
  });

  it('composite PK lists all fields', () => {
    const defs = defineStore({
      tags: defineTable({
        tableName: 'tags',
        schema: z.object({ userId: z.string(), tag: z.string() }),
        primaryKey: ['userId', 'tag'],
      }),
    });
    const code = generateSchemaCode(defs);
    expect(code).toContain('primaryKey({ columns: [t.userId, t.tag] })');
  });

  it('non-unique index uses index()', () => {
    const defs = defineStore({
      users: defineTable({
        tableName: 'users',
        schema: z.object({ id: z.string(), age: z.number().int() }),
        primaryKey: 'id',
        indexes: [{ columns: ['age'] }],
      }),
    });
    const code = generateSchemaCode(defs);
    expect(code).toContain("index('users_age_idx').on(t.age)");
    expect(code).toContain('import {');
    expect(code).toContain('index');
  });

  it('unique index uses unique()', () => {
    const defs = defineStore({
      users: defineTable({
        tableName: 'users',
        schema: z.object({ id: z.string(), email: z.string() }),
        primaryKey: 'id',
        indexes: [{ columns: ['email'], unique: true }],
      }),
    });
    const code = generateSchemaCode(defs);
    expect(code).toContain("unique('users_email_idx').on(t.email)");
  });

  it('named index uses the provided name', () => {
    const defs = defineStore({
      users: defineTable({
        tableName: 'users',
        schema: z.object({ id: z.string(), email: z.string() }),
        primaryKey: 'id',
        indexes: [{ columns: ['email'], name: 'my_email_idx' }],
      }),
    });
    const code = generateSchemaCode(defs);
    expect(code).toContain("index('my_email_idx').on(t.email)");
  });

  it('compound index joins columns with comma', () => {
    const defs = defineStore({
      posts: defineTable({
        tableName: 'posts',
        schema: z.object({
          id: z.string(),
          userId: z.string(),
          status: z.string(),
        }),
        primaryKey: 'id',
        indexes: [{ columns: ['userId', 'status'] }],
      }),
    });
    const code = generateSchemaCode(defs);
    expect(code).toContain(
      "index('posts_userId_status_idx').on(t.userId, t.status)",
    );
  });

  it('exports a named const for each table key', () => {
    const defs = defineStore({
      users: defineTable({
        tableName: 'users',
        schema: z.object({ id: z.string() }),
        primaryKey: 'id',
      }),
      posts: defineTable({
        tableName: 'posts',
        schema: z.object({ id: z.string() }),
        primaryKey: 'id',
      }),
    });
    const code = generateSchemaCode(defs);
    expect(code).toContain("export const users = sqliteTable('users'");
    expect(code).toContain("export const posts = sqliteTable('posts'");
  });

  it('throws for truly unsupported Zod types (e.g. z.never)', () => {
    const defs = defineStore({
      bad: defineTable({
        tableName: 'bad',
        schema: z.object({ id: z.string(), x: z.never() }),
        primaryKey: 'id',
      }),
    });
    expect(() => generateSchemaCode(defs, { settings: false })).toThrow(
      /unsupported Zod type/,
    );
  });
});

