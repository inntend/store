// ─── generateDrizzleConfig — writes two files (lines 198-216) ────────────────

describe('generateDrizzleConfig — writes schema and config files', () => {
  it('writes to explicit paths', () => {
    const logSpy = vi.spyOn(console, 'log').mockImplementation(() => {});
    const dir = mkdtempSync(join(tmpdir(), 'inntend-store-test-'));
    try {
      const Schema = z.object({ id: z.string(), label: z.string() });
      const defs = defineStore({
        items: defineTable({
          tableName: 'items',
          schema: Schema,
          primaryKey: 'id',
        }),
      });
      generateDrizzleConfig(defs, {
        schemaOut: join(dir, 'schema.ts'),
        configOut: join(dir, 'drizzle.config.ts'),
      });
      expect(readFileSync(join(dir, 'schema.ts'), 'utf8')).toContain(
        'sqliteTable',
      );
      expect(readFileSync(join(dir, 'drizzle.config.ts'), 'utf8')).toContain(
        'defineConfig',
      );
    } finally {
      logSpy.mockRestore();
      rmSync(dir, { recursive: true });
    }
  });

  it('uses default filenames when no options provided (covers ?? fallbacks)', () => {
    const logSpy = vi.spyOn(console, 'log').mockImplementation(() => {});
    const dir = mkdtempSync(join(tmpdir(), 'inntend-store-defaults-'));
    const origCwd = process.cwd();
    try {
      process.chdir(dir);
      const Schema = z.object({ id: z.string() });
      const defs: Record<string, any> = {
        items: defineTable({
          tableName: 'items',
          schema: Schema,
          primaryKey: 'id',
        }),
        settings: createSettingsDef(),
      };
      generateDrizzleConfig(defs);
      expect(readFileSync(join(dir, 'schema.ts'), 'utf8')).toContain(
        'sqliteTable',
      );
      expect(readFileSync(join(dir, 'drizzle.config.ts'), 'utf8')).toContain(
        'defineConfig',
      );
    } finally {
      logSpy.mockRestore();
      process.chdir(origCwd);
      rmSync(dir, { recursive: true });
    }
  });
});

